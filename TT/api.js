// a function for choosing a year
function changeYear() {
  let yearV = year.value;
  if (yearV == 2024) {
    year2024.classList.remove('none');
    year2024.classList.add('d-flex');
    year2023.classList.add('none'); 
    packMarch2024()
  }
  else if (yearV == 2023) {
    year2023.classList.remove('none');
    year2023.classList.add('d-flex');
    year2024.classList.add('none');
    location.reload();
  }
}

