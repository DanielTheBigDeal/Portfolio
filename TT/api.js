function showCard(arr) {
  for (const item of arr) {
    $Event = document.createElement('li');
    $Event.classList.add('events__event');

    const $EventDate = document.createElement('h3');
    $EventDate.classList.add('events__date');

    const logoImg = {
      src: item.logo,
      alt: 'Логотип конференции',
    };
    const $EventLogo = document.createElement('span');
    const $imageElement = document.createElement('img');
    $imageElement.src = logoImg.src;
    $imageElement.alt = logoImg.alt;
    $imageElement.style.width = '80.89px';
    $imageElement.style.height = '90px';
    $EventLogo.append($imageElement);
    
    const $EventName = document.createElement('h2');
    $EventName.classList.add('events__name');

    const $EventDescr = document.createElement('p');
    $EventDescr.classList.add('events__description');

    const $EventLocation = document.createElement('p');
    $EventLocation.classList.add('events__location');

    const $EventLink = document.createElement('a');
    $EventLink.classList.add('events__link');

    $EventDate.textContent = item.date_range;
    $EventName.textContent = item.name;
    $EventDescr.textContent = item.brief;
    $EventLocation.textContent = item.location;
    $EventLink.href = item.url;
    $EventLink.textContent = item.url.replace('https://', '');

    let buy = item.buy;
    const $EventBuy = document.createElement('span');
    $EventBuy.classList.add('events__buy');
    const $EventBtn = document.createElement('button');
    $EventBtn.classList.add('events__btn');
    const $EventTicketLink = document.createElement('a');
    $EventTicketLink.classList.add('events__btn__link');

    $EventTicketLink.href = buy.link;
    $EventTicketLink.textContent = 'Купить билет';
    $EventBtn.append($EventTicketLink);

    const $EventAbout = document.createElement('a');
    $EventAbout.href = item.url;
    $EventAbout.textContent = 'Подробнее';
    $EventAbout.classList.add('events__more')
    
    $EventBuy.append($EventBtn, $EventAbout);

    $Event.append($EventDate, $EventLogo, $EventName, $EventDescr, $EventLocation, $EventLink, $EventBuy);
    $eventsList.append($Event);
  }
}

